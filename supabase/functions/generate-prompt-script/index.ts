import { serve } from "https://deno.land/std@0.168.0/http/server.ts";
import "https://deno.land/x/xhr@0.1.0/mod.ts";
import OpenAI from "https://esm.sh/openai@4.28.0";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const openai = new OpenAI({
      apiKey: Deno.env.get('OPENAI_API_KEY'),
    });

    const { prompt, text } = await req.json();
    console.log('Generating script with prompt:', prompt);
    console.log('For text:', text);

    const completion = await openai.chat.completions.create({
      model: "gpt-4o-mini",
      messages: [
        {
          role: "system",
          content: "You are a creative script writer that helps create engaging social media content based on user prompts."
        },
        {
          role: "user",
          content: `${prompt}\n\nThis is the Script:\n\n${text}`
        }
      ],
    });

    const scriptText = completion.choices[0]?.message?.content;
    if (!scriptText) {
      throw new Error('Failed to generate script');
    }

    console.log('Generated script:', scriptText);

    return new Response(
      JSON.stringify({ text: scriptText }),
      { 
        headers: { 
          ...corsHeaders,
          'Content-Type': 'application/json'
        }
      }
    );

  } catch (error) {
    console.error('Error in generate-prompt-script function:', error);
    return new Response(
      JSON.stringify({ error: error.message }), 
      { 
        status: 500,
        headers: { 
          ...corsHeaders,
          'Content-Type': 'application/json'
        }
      }
    );
  }
});